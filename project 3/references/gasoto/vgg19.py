def VGG_16(inputshape):
    model = Sequential()
    model.add(BatchNormalization(input_shape=inputshape))
    model.add(ZeroPadding2D((1,1)))
    model.add(Convolution2D(64, 3, 3, activation='relu'))
    model.add(ZeroPadding2D((1,1)))
    model.add(Convolution2D(64, 3, 3, activation='relu'))
